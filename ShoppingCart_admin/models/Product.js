// Tạo lớp đối tượng Product
function Product(id, name, price, img, desc,screen,backCamera,frontCamera,type) {
  // thuộc tính (properties)
  this.id = id;
  this.name = name;
  this.price = price;
  this.img = img;
  this.desc = desc;
  this.screen=screen;
  this.backCamera=backCamera;
  this.frontCamera=frontCamera;
  this.type=type;
}

